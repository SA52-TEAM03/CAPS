package CA.CAPS.util;

public class Grade {
	
	private String gradePoint;
	private String grade;
	
	public Grade (String gradePoint, String grade) {			
		this.gradePoint = gradePoint;
		this.grade = grade;
	}
	
	public String getGradePoint() {
		return gradePoint;
	}	

	public String getGrade() {
		return grade;
	}	

}
